package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        //'c', 'h', 'a', 'r'
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';
        int ic = c;
        int ih = h;
        int ia = a;
        int ir = r;
        System.out.println(ic);
        System.out.println(ih);
        System.out.println(ia);
        System.out.println(ir);
    }
}
